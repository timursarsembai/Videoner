import { isPaidVideoQuality } from './paid-quality';

// Общий набор кейсов для сверки трёх мест, где продублировано правило "какое
// качество видео платное" (порог 720p): здесь (сервер, авторитетный
// источник), bot/src/paid-quality.test.ts, web/lib/subscription.test.ts.
// Кейсы одинаковые по смыслу во всех трёх файлах (буквально скопированы —
// это тестовые данные, не логика; сервисы физически изолированы друг от
// друга разными Docker build context'ами, общий импортируемый фикстур-файл
// сюда не докопируется). Меняешь порог/правило — обнови все три файла и
// прогони тесты в каждом сервисе.
const cases: [name: string, quality: string, available: string[], expected: boolean][] = [
  ['ниже порога — бесплатно', '480', ['480', '720', '1080'], false],
  ['ровно 720p, но есть варианты ниже — платно', '720', ['480', '720', '1080'], true],
  ['ровно 720p — единственный/минимальный вариант — бесплатно', '720', ['720', '1080'], false],
  ['выше 720p — всегда платно, даже если единственный вариант', '1080', ['1080'], true],
  ['выше 720p при наличии более низких вариантов — платно', '1080', ['720', '1080'], true],
  ['нечисловое качество (Facebook hd/sd) — всегда бесплатно', 'hd', ['hd', 'sd'], false],
  ['720p без информации о доступных качествах — бесплатно (fallback)', '720', [], false],
  ['совсем низкое качество — бесплатно', '144', ['144', '720'], false],
];

describe('isPaidVideoQuality (server, авторитетная реализация)', () => {
  it.each(cases)('%s', (_name, quality, available, expected) => {
    expect(isPaidVideoQuality(quality, available)).toBe(expected);
  });
});
